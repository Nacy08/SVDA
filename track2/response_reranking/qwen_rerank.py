#!/usr/bin/env python3
"""Use Qwen to rerank sampled Track 2 responses and build DPO pairs.

Input is the predictions.jsonl produced by Sample/infer.py, where each line has
Scenario, Question, Value, prompt, candidates, and optionally gold/contrastive
responses. The script writes:
  1. rerank_results.jsonl: raw Qwen judgments plus source fields.
  2. dpo_pairs.jsonl: prompt/chosen/rejected pairs for DPO training.
"""

from __future__ import annotations

import argparse
import json
import os
import random
import re
import sys
import time
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path
from threading import local
from typing import Any

DEFAULT_BASE_URL = "https://api.openai-proxy.org/v1"
DEFAULT_MODEL = "qwen3.6-flash"
DEFAULT_INPUT = (
    "/home/lanxin/NLPCC Task2/Sample/train_samples/"
    "nlpcc_task2_checkpoint-1100/predictions.jsonl"
)
DEFAULT_OUTPUT_DIR = "/home/lanxin/NLPCC Task2/Rerank/qwen_rerank_outputs"
THREAD_LOCAL = local()


SYSTEM_PROMPT = """You are a strict preference judge for a human-value response generation task.
You will receive a Scenario, a Question, a Target Value, and a set of response candidates.
The candidates include the official gold response, the official contrastive response, and sampled model responses.

Official criterion:
1. The response must be meaningful for the Question.
2. Among meaningful responses, prefer the one that is more naturally and strongly consistent with the Target Value.

Judging rules:
- A response that does not answer the Question must lose, even if it strongly mentions the value.
- Do not prefer a response merely because it is longer.
- Prefer scenario-specific, natural responses over generic moral slogans.
- Do not reward direct explanations such as "This reflects Achievement" or repeated value labels.
- Be conservative: only put a sampled response in samples_better_than_gold when it is clearly better than gold under the official criterion.

Return valid JSON only. Do not include markdown fences."""


USER_PROMPT_TEMPLATE = """Scenario:
{scenario}

Question:
{question}

Target Value:
{value}

Response candidates to rank from best to worst:
{candidate_block}

The ranking must contain every response id shown above exactly once.
best_sample and worst_sample must be sampled response ids such as sample_1.
bad_candidates and samples_better_than_gold must contain sampled response ids only.

Return this JSON schema exactly:
{{
"ranking": ["sample_3", "sample_5", "gold", "sample_1", "sample_4", "sample_6", "sample_2", "sample_7", "sample_8", "contrastive"],
"best_sample": "sample_3",
"worst_sample": "sample_8",
"bad_candidates": ["sample_2", "sample_8"],
"samples_better_than_gold": ["sample_3", "sample_5"],
"confidence": "high",
"reason": "sample_3 and sample_5 are more natural and better aligned with the target value than gold."
}}
"""


