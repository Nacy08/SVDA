#!/usr/bin/env python3
"""Evaluate GPT-5.1-reranked DPO predictions with the same pairwise judge prompt.

This script is a standalone version of Codex/之前代码/06_eval_pairwise.py for
the current DPO rerank output. It keeps the same evaluation prompt and default
judge model, while removing the dependency on the old common.py helpers.
"""

from __future__ import annotations

import argparse
import csv
import json
import os
import time
from pathlib import Path
from typing import Any

DEFAULT_GENERATION_FILE = (
    "/home/lanxin/NLPCC Task2/Rerank/gpt51_dpo_rerank_outputs/final_predictions.jsonl"
)
DEFAULT_OUTPUT_DIR = "/home/lanxin/NLPCC Task2/Rerank/gpt51_dpo_eval_outputs"
DEFAULT_JUDGE_MODEL = "deepseek-v4-pro"
DEFAULT_JUDGE_BASE_URL = "https://api.deepseek.com"


PROMPT_TEMPLATE = """You are a strict evaluator for a response generation task based on the 19 basic human values in Schwartz's theory.

[Evaluation Goal]
You will be given a scenario, a question, a target human value, and two candidate responses.

Your goal is to decide which response better answers the question while aligning with the target human value: {Value}.

A better response should satisfy the following criteria:
1. It answers the question naturally, clearly, and meaningfully.
2. It is consistent with the role and situation described in the scenario.
3. It aligns more strongly with the given target human value.
4. It does not merely repeat the name of the target value.
5. It is not vague, generic, off-topic, templated, or unnatural.
6. If the two responses are similar in overall quality, choose "tie".

Do not assume either response is the reference answer. Judge only based on the criteria above.

[Scenario]
{Scenario}

[Question]
{Question}

[Target Human Value]
{Value}

[Candidate Response A]
{Response_A}

[Candidate Response B]
{Response_B}

Return JSON only:
{{
  "winner": "A" | "B" | "tie",
  "content_winner": "A" | "B" | "tie",
  "value_winner": "A" | "B" | "tie",
  "response_a_content_score": 1-5,
  "response_b_content_score": 1-5,
  "response_a_value_score": 1-5,
  "response_b_value_score": 1-5,
  "response_a_overall_score": 1-5,
  "response_b_overall_score": 1-5,
  "reason": "Briefly explain the judgment in one sentence."
}}"""


SUMMARY_FIELDS = [
    "checkpoint",
    "generation_file",
    "total_count",
    "win_count",
    "tie_count",
    "lose_count",
    "failed_count",
    "win_rate",
    "tie_rate",
    "lose_rate",
    "pairwise_score",
    "avg_prediction_content_score",
    "avg_prediction_value_score",
    "avg_prediction_overall_score",
]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Evaluate GPT-5.1-reranked DPO final predictions with pairwise judge."
    )
    parser.add_argument(
        "--generation_file",
        default=DEFAULT_GENERATION_FILE,
        help="final_predictions.jsonl generated by gpt51_rerank_dpo_candidates.py.",
    )
    parser.add_argument(
        "--output_dir",
        default=DEFAULT_OUTPUT_DIR,
        help="Directory for judge_results.jsonl, failed_cases.jsonl, and summaries.",
    )
    parser.add_argument(
        "--judge_model",
        default=os.getenv("JUDGE_MODEL", DEFAULT_JUDGE_MODEL),
        help="Judge model. Default matches the old script: deepseek-v4-pro.",
    )
    parser.add_argument(
        "--judge_base_url",
        default=os.getenv("JUDGE_BASE_URL")
        or os.getenv("DEEPSEEK_API_BASE")
        or DEFAULT_JUDGE_BASE_URL,
        help="OpenAI-compatible judge API base URL.",
    )
    parser.add_argument(
        "--api_key_env",
        default="DEEPSEEK_API_KEY",
        help="Environment variable that stores the judge API key.",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=None,
        help="Optional row limit for smoke tests.",
    )
    parser.add_argument(
        "--resume",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Skip already completed non-failed cases. Default: true.",
    )
    parser.add_argument(
        "--max_retries",
        type=int,
        default=3,
        help="Maximum API retries per case.",
    )
    parser.add_argument(
        "--request_interval",
        type=float,
        default=0.0,
        help="Seconds to sleep between requests.",
    )
    parser.add_argument(
        "--request_timeout",
        type=float,
        default=120.0,
        help="Request timeout in seconds.",
    )
    parser.add_argument(
        "--max_tokens",
        type=int,
        default=2048,
        help="Maximum judge response tokens.",
    )
    parser.add_argument(
        "--temperature",
        type=float,
        default=0.0,
        help="Judge temperature when thinking is disabled.",
    )
    parser.add_argument(
        "--thinking",
        choices=["enabled", "disabled"],
        default="disabled",
        help="DeepSeek thinking mode, matching the old script's extra_body shape.",
    )
    parser.add_argument(
        "--reasoning_effort",
        default="high",
        help="Reasoning effort when thinking is enabled.",
    )
    return parser.parse_args()


def read_jsonl(path: Path, limit: int | None = None, add_id: bool = False) -> list[dict[str, Any]]:
    rows = []
    with path.open("r", encoding="utf-8") as f:
        for line_no, line in enumerate(f, start=1):
            if not line.strip():
                continue
            row = json.loads(line)
            if add_id and "id" not in row:
                row["_line_no"] = line_no
            rows.append(row)
            if limit is not None and len(rows) >= limit:
                break
    return rows


def write_jsonl(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")


def append_jsonl(path: Path, row: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("a", encoding="utf-8") as f:
        f.write(json.dumps(row, ensure_ascii=False) + "\n")


def normalize_base_url(base_url: str) -> str:
    return base_url.strip().rstrip("/")


def require_env(name: str) -> str:
    value = os.getenv(name)
    if not value:
        raise SystemExit(
            f"Missing API key. Please set {name}, for example:\n"
            f"  export {name}=sk-..."
        )
    if not value.isascii():
        raise SystemExit(
            f"{name} contains non-ASCII characters. Please export the real API key, "
            "not a Chinese placeholder."
        )
    return value


def make_client(args: argparse.Namespace):
    try:
        from openai import OpenAI
    except ImportError as exc:
        raise SystemExit("openai is not installed. Install it with: pip install openai") from exc

    return OpenAI(
        api_key=require_env(args.api_key_env),
        base_url=normalize_base_url(args.judge_base_url),
        timeout=args.request_timeout,
    )


def extract_json(text: str) -> dict[str, Any]:
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        start = text.find("{")
        end = text.rfind("}")
        if start >= 0 and end > start:
            return json.loads(text[start : end + 1])
        raise


def case_key(row: dict[str, Any]) -> str:
    return str(row.get("id", row.get("_line_no", "")))


def checkpoint_from(generation_file: str, rows: list[dict[str, Any]]) -> str:
    for row in rows:
        if row.get("checkpoint"):
            return str(row["checkpoint"])
        if row.get("adapter_name"):
            return str(row["adapter_name"])
    stem = Path(generation_file).stem
    return stem.replace("dev_final_", "")


def load_completed(path: Path) -> dict[str, dict[str, Any]]:
    completed = {}
    if not path.exists():
        return completed
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            if not line.strip():
                continue
            row = json.loads(line)
            completed[str(row.get("id"))] = row
    return completed


def call_judge(prompt: str, args: argparse.Namespace, client) -> str:
    request_kwargs: dict[str, Any] = {
        "model": args.judge_model,
        "messages": [
            {"role": "system", "content": "You are a strict evaluator. Always return valid JSON only."},
            {"role": "user", "content": prompt},
        ],
        "max_tokens": args.max_tokens,
        "response_format": {"type": "json_object"},
        "stream": False,
        "timeout": args.request_timeout,
        "extra_body": {"thinking": {"type": args.thinking}},
    }
    if args.thinking == "enabled":
        request_kwargs["reasoning_effort"] = args.reasoning_effort
    else:
        request_kwargs["temperature"] = args.temperature
    response = client.chat.completions.create(**request_kwargs)
    content = response.choices[0].message.content or ""
    if not content.strip():
        raise RuntimeError("Judge API returned empty content.")
    return content


def judge_one(row: dict[str, Any], args: argparse.Namespace, client) -> tuple[str, dict[str, Any] | None, str | None]:
    prompt = PROMPT_TEMPLATE.format(
        Scenario=row.get("Scenario", ""),
        Question=row.get("Question", ""),
        Value=row.get("Value", ""),
        Response_A=row.get("Consistent Value Response", ""),
        Response_B=row.get("prediction", ""),
    )
    last_error = None
    last_raw = ""
    for attempt in range(1, args.max_retries + 1):
        try:
            raw = call_judge(prompt, args, client)
            return raw, extract_json(raw), None
        except Exception as exc:  # Keep batch evaluation resumable.
            last_error = str(exc)
            if attempt < args.max_retries:
                time.sleep(min(2 ** (attempt - 1), 8))
    return last_raw, None, last_error


def prediction_result(parsed: dict[str, Any] | None) -> str:
    if not parsed:
        return "failed"
    winner = str(parsed.get("winner", "")).strip().lower()
    if winner == "b":
        return "win"
    if winner == "a":
        return "lose"
    if winner == "tie":
        return "tie"
    return "failed"


def score(parsed: dict[str, Any] | None, key: str) -> float | None:
    if not parsed:
        return None
    try:
        return float(parsed.get(key))
    except (TypeError, ValueError):
        return None


def build_result(
    row: dict[str, Any],
    checkpoint: str,
    raw: str,
    parsed: dict[str, Any] | None,
    error: str | None = None,
) -> dict[str, Any]:
    return {
        "checkpoint": checkpoint,
        "id": case_key(row),
        "value": row.get("Value", ""),
        "reference": row.get("Consistent Value Response", ""),
        "prediction": row.get("prediction", ""),
        "best_candidate": row.get("best_candidate", ""),
        "rerank_reason": row.get("rerank_reason", ""),
        "rerank_judge_model": row.get("judge_model", ""),
        "judge_model": row.get("_eval_judge_model", ""),
        "raw_response": raw,
        "parsed_response": parsed,
        "prediction_result": "failed" if error else prediction_result(parsed),
        "prediction_content_score": score(parsed, "response_b_content_score"),
        "prediction_value_score": score(parsed, "response_b_value_score"),
        "prediction_overall_score": score(parsed, "response_b_overall_score"),
        "reason": "" if not parsed else parsed.get("reason", ""),
        "error": error,
    }


def average(values: list[Any]) -> float:
    clean = [v for v in values if isinstance(v, (int, float))]
    return sum(clean) / len(clean) if clean else 0.0


def summarize(checkpoint: str, generation_file: str, results: list[dict[str, Any]]) -> dict[str, Any]:
    total_count = len(results)
    win_count = sum(1 for r in results if r.get("prediction_result") == "win")
    tie_count = sum(1 for r in results if r.get("prediction_result") == "tie")
    lose_count = sum(1 for r in results if r.get("prediction_result") == "lose")
    failed_count = sum(1 for r in results if r.get("prediction_result") == "failed")
    judged_count = win_count + tie_count + lose_count
    denominator = judged_count or total_count or 1
    win_rate = win_count / denominator
    tie_rate = tie_count / denominator
    lose_rate = lose_count / denominator
    return {
        "checkpoint": checkpoint,
        "generation_file": str(generation_file),
        "total_count": total_count,
        "win_count": win_count,
        "tie_count": tie_count,
        "lose_count": lose_count,
        "failed_count": failed_count,
        "win_rate": win_rate,
        "tie_rate": tie_rate,
        "lose_rate": lose_rate,
        "pairwise_score": win_rate + 0.5 * tie_rate,
        "avg_prediction_content_score": average([r.get("prediction_content_score") for r in results]),
        "avg_prediction_value_score": average([r.get("prediction_value_score") for r in results]),
        "avg_prediction_overall_score": average([r.get("prediction_overall_score") for r in results]),
    }


def update_summary_csv(path: Path, summary: dict[str, Any]) -> None:
    rows = []
    if path.exists():
        with path.open("r", encoding="utf-8", newline="") as f:
            rows = list(csv.DictReader(f))
    new_rows = []
    replaced = False
    for row in rows:
        if row.get("checkpoint") == summary["checkpoint"]:
            new_rows.append({field: summary.get(field, "") for field in SUMMARY_FIELDS})
            replaced = True
        else:
            new_rows.append(row)
    if not replaced:
        new_rows.append({field: summary.get(field, "") for field in SUMMARY_FIELDS})
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=SUMMARY_FIELDS)
        writer.writeheader()
        writer.writerows(new_rows)


def write_run_config(path: Path, args: argparse.Namespace, checkpoint: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    payload = {
        "generation_file": args.generation_file,
        "output_dir": args.output_dir,
        "checkpoint": checkpoint,
        "judge_model": args.judge_model,
        "judge_base_url": args.judge_base_url,
        "api_key_env": args.api_key_env,
        "limit": args.limit,
        "resume": args.resume,
        "max_retries": args.max_retries,
        "request_interval": args.request_interval,
        "request_timeout": args.request_timeout,
        "max_tokens": args.max_tokens,
        "temperature": args.temperature,
        "thinking": args.thinking,
        "reasoning_effort": args.reasoning_effort,
    }
    with path.open("w", encoding="utf-8") as f:
        json.dump(payload, f, ensure_ascii=False, indent=2)
        f.write("\n")


def main() -> None:
    args = parse_args()
    rows = read_jsonl(Path(args.generation_file), limit=args.limit, add_id=True)
    checkpoint = checkpoint_from(args.generation_file, rows)
    output_dir = Path(args.output_dir)
    checkpoint_dir = output_dir / checkpoint
    checkpoint_dir.mkdir(parents=True, exist_ok=True)

    result_path = checkpoint_dir / "judge_results.jsonl"
    failed_path = checkpoint_dir / "failed_cases.jsonl"
    summary_path = checkpoint_dir / "judge_summary.json"
    completed = load_completed(result_path)
    write_run_config(checkpoint_dir / "run_config.json", args, checkpoint)

    client = make_client(args)
    for row in rows:
        row["_eval_judge_model"] = args.judge_model
        key = case_key(row)
        if args.resume and key in completed and completed[key].get("prediction_result") != "failed":
            continue
        raw, parsed, error = judge_one(row, args, client)
        result = build_result(row, checkpoint, raw, parsed, error=error)
        append_jsonl(result_path, result)
        completed[key] = result
        if result["prediction_result"] == "failed":
            append_jsonl(failed_path, result)
        if args.request_interval > 0:
            time.sleep(args.request_interval)

    results = list(load_completed(result_path).values())
    write_jsonl(failed_path, [r for r in results if r.get("prediction_result") == "failed"])
    summary = summarize(checkpoint, args.generation_file, results)
    with summary_path.open("w", encoding="utf-8") as f:
        json.dump(summary, f, ensure_ascii=False, indent=2)
        f.write("\n")
    update_summary_csv(output_dir / "dpo_rerank_pairwise_scores.csv", summary)
    print(json.dumps(summary, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
